package Modal;

public class Athlete {

	private int athlete_id;
	private String athlete_name;
	private String athlete_gender;
	private String category;
	private String athlete_email;
	private int athlete_mobile;
	
	public int getAthlete_id() {
		return athlete_id;
	}
	public void setAthlete_id(int athlete_id) {
		this.athlete_id = athlete_id;
	}
	public String getAthlete_name() {
		return athlete_name;
	}
	public void setAthlete_name(String athlete_name) {
		this.athlete_name = athlete_name;
	}
	public String getAthlete_gender() {
		return athlete_gender;
	}
	public void setAthlete_gender(String athlete_gender) {
		this.athlete_gender = athlete_gender;
	}
	public String getCategory() {
		return category;
	}
	public void setCategory(String category) {
		this.category = category;
	}
	public String getAthlete_email() {
		return athlete_email;
	}
	public void setAthlete_email(String athlete_email) {
		this.athlete_email = athlete_email;
	}
	public int getAthlete_mobile() {
		return athlete_mobile;
	}
	public void setAthlete_mobile(int athlete_mobile) {
		this.athlete_mobile = athlete_mobile;
	}
	
	
	
}
